    private void jButton52ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton52ActionPerformed
        String busqueda = jTextFieldBusquedaTienda.getText();
        if (busqueda.length() != 0) {
            DefaultTableModel modelo = (DefaultTableModel) jTableVideos.getModel();
            modelo.setRowCount(0);
            ArrayList<String[]> datos = new ArrayList<>();
            switch (jComboBoxBusquedaTienda.getSelectedIndex()) {
                case 0:
                    if (comprobarInt(busqueda)) {
                        datos = capaLogica.getVideosId(busqueda);
                    }
                    break;
                case 1:
                    datos = capaLogica.getVideosFecha(busqueda);
                    break;
                case 2:
                    datos = capaLogica.getVideosCalificacion(busqueda);
                    break;
                case 3:
                    datos = capaLogica.getVideosCategoria(busqueda);
                    break;
                case 4:
                    datos = capaLogica.getVideosUsuario(busqueda);
                    break;
                case 5:
                    datos = capaLogica.getVideosValor(busqueda);
                    break;
            }
            for (String[] row : datos) {
                modelo.addRow(row);
            }
        }
    }//GEN-LAST:event_jButton52ActionPerformed

